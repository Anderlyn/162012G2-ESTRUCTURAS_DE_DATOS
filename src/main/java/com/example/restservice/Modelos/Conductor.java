
package com.example.restservice.Modelos;

public class Conductor {

    private int id;
    private String nombre;

    public Conductor(int id, String nombre) {
        this.id = id;
        this.nombre = nombre;
    }

    public int getId() {
        return id;
    }

    public String getNombre() {
        return nombre;
    }
    
    
    
}
